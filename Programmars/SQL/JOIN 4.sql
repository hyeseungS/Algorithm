SELECT T1.CAR_ID, T2.CAR_TYPE, ROUND(T1.DAILY_FEE*(1-(T2.DISCOUNT_RATE/100))*30, 0) AS FEE
FROM CAR_RENTAL_COMPANY_CAR AS T1
JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN AS T2
ON T1.CAR_TYPE = T2.CAR_TYPE AND T2.DURATION_TYPE = '30일 이상'
WHERE T1.CAR_TYPE IN ('세단', 'SUV') AND
T1.CAR_ID NOT IN (SELECT CAR_ID FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY WHERE START_DATE <= '2022-11-30' AND END_DATE >= '2022-11-01')
HAVING FEE >= 500000 AND FEE <= 2000000
ORDER BY FEE DESC, CAR_TYPE ASC, CAR_ID DESC;